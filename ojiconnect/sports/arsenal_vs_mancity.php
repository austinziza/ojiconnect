While living with his aunt and uncle in Elsburg, near an Afrikaner village, Peter is introduced to the rural life dictated by the work for white people and has to learn the brutal lesson that, no matter what happens, he must never lift his hand to a white person. Back in Johannesburg, young Peter soon adapts to the life in a slum, follows a gang of other young boys and together they steal and fight other gangs. However, once he is introduced to literature by a Jewish girl at work, Peter longs for education and a new part of his life begins. “I was ripe for something new, the new things my books had revealed, to take the place of the old life.” (163)<br><br>


Book Two of the autobiography deals with his further education that begins with his work at the Bantu Men’s Social Center in Johannesburg where he is introduced to important thinkers not only among the South Africans: “Du Bois’s words had the impact of a revelation” (193). Especially African American authors of the so-called Harlem Renaissance like Countee Cullen, Langston Hughes and Claude McKay shape his consciousness of racial injustice, of the need for change and his wish to become a writer himself. When he is accepted at a missionary college near Pietersburg, Peter leaves his family and his girlfriend behind to seize the chance for a higher education and his way out of the slums of Johannesburg. Soon he is completely absorbed by his studies and becomes a full member of the church after his confirmation and First Communion. But when he starts to measure the Christian ideology of the mission fathers against the world of colour and race that he experiences outside of the somewhat protected setting of college, he decides not to become a teacher and quits college.
Book Three begins in 1938, when Peter is 19 years old. He is back in Johannesburg and comes into contact with Marxist ideology, makes his first white friends and further works on his writing. At that time he already has a reputation as revolutionary poet because The Bantu World, a black South African newspaper, has published some of his works by then. For a short time, he even has a relationship with a white woman but they soon break up because.
